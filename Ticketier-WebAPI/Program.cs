
using Microsoft.EntityFrameworkCore;
using Ticketier_WebAPI.Core.AutoMapperConfig;
using Ticketier_WebAPI.Core.Context;

namespace Ticketier_WebAPI
{
    public class Program
    {
        public static void Main(string[] args)
        {
            var builder = WebApplication.CreateBuilder(args);

            // Add services to the container.

            builder.Services.AddControllers();
            // Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
            builder.Services.AddEndpointsApiExplorer();
            builder.Services.AddSwaggerGen();

            builder.Services.AddDbContext<ApplicationDbContext>(options =>
            {
                var connictionString = builder.Configuration.GetConnectionString("Local");
                options.UseSqlServer(connictionString);
            });
            builder.Services.AddAutoMapper(typeof(AutoMapperConfigProfile));

            var app = builder.Build();

            // Configure the HTTP request pipeline.
            if (app.Environment.IsDevelopment())
            {
                app.UseSwagger();
                app.UseSwaggerUI();
            }

            app.UseHttpsRedirection();

            app.UseAuthorization();


            app.MapControllers();

            app.Run();
        }
    }
}
